volatile object* create_kul_sprite() {
    volatile object *sprite = &OAM_MEM[1];
    int x, y;

    sprite->attr0 = y |     /* y coordinate */
        (0 << 8) |          /* rendering mode */
        (0 << 10) |         /* gfx mode */
        (0 << 12) |         /* mosaic */
        (1 << 13) |         /* color mode, 0:16, 1:256 */
        (1 << 14); 			/* 64x32: bit 1 */
    sprite->attr1 = x |     /* x coordinate */
        (0 << 9) |          /* affine flag */
        (0 << 12) |         /* horizontal flip flag */
        (0 << 13) |         /* vertical flip flag */
        (3 << 14);  		/* 64x32: bit 3 */
    sprite->attr2 = 0; // vanaf de 2de tile, palet 0

    return sprite;
}

void vsync() {
    while (REG_VCOUNT >= 160);
    while (REG_VCOUNT < 160);
}

